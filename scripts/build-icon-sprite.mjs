#!/usr/bin/env node
/**
 * DL-312: Build an SVG sprite from Lucide icons for the admin panel.
 *
 * Reads icon names from scripts/icon-list.txt, downloads each Lucide source SVG
 * from the unpkg CDN (no npm devDep needed), extracts its inner markup, and
 * emits a single sprite file at frontend/assets/icons/icons.svg containing
 * <symbol id="icon-NAME" ...> for each entry.
 *
 * Usage:
 *   node scripts/build-icon-sprite.mjs
 *
 * When a new icon is needed: add the name to scripts/icon-list.txt (sorted),
 * rerun this script, commit both files.
 */
import { readFileSync, writeFileSync, mkdirSync } from 'node:fs';
import { dirname, resolve } from 'node:path';
import { fileURLToPath } from 'node:url';

const SCRIPT_DIR = dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = resolve(SCRIPT_DIR, '..');
const ICON_LIST = resolve(REPO_ROOT, 'scripts', 'icon-list.txt');
const OUT_PATH = resolve(REPO_ROOT, 'frontend', 'assets', 'icons', 'icons.svg');
const LUCIDE_VERSION = '0.576.0';
const CDN = (name) => `https://unpkg.com/lucide-static@${LUCIDE_VERSION}/icons/${name}.svg`;

async function fetchIcon(name) {
    const url = CDN(name);
    let res;
    let lastErr;
    for (let attempt = 1; attempt <= 4; attempt++) {
        try {
            res = await fetch(url);
            if (res.ok) break;
            lastErr = new Error(`HTTP ${res.status}`);
        } catch (err) {
            lastErr = err;
        }
        if (attempt < 4) await new Promise((r) => setTimeout(r, 250 * attempt));
    }
    if (!res || !res.ok) throw new Error(`Fetch failed for "${name}" (${url}): ${lastErr?.message || 'unknown'}`);
    const svg = await res.text();
    const viewBoxMatch = svg.match(/viewBox="([^"]+)"/);
    if (!viewBoxMatch) throw new Error(`No viewBox in SVG for "${name}"`);
    const viewBox = viewBoxMatch[1];
    // Grab everything between the outer <svg ...> and </svg>
    const innerMatch = svg.match(/<svg[^>]*>([\s\S]*)<\/svg>/);
    if (!innerMatch) throw new Error(`Could not extract inner SVG for "${name}"`);
    const inner = innerMatch[1].trim();
    return { name, viewBox, inner };
}

async function main() {
    const names = readFileSync(ICON_LIST, 'utf8')
        .split('\n')
        .map((s) => s.trim())
        .filter((s) => s && !s.startsWith('#'));
    if (names.length === 0) throw new Error('icon-list.txt is empty');

    console.log(`Fetching ${names.length} Lucide icons (v${LUCIDE_VERSION})…`);
    const symbols = [];
    // Stroke attributes set ON the symbol element so they apply to all child paths
    // via SVG presentation-attribute inheritance, regardless of how the outer
    // <svg><use href="#sym"/></svg> wrapper attributes propagate through the
    // <use> shadow tree (which varies by browser). Lucide's source paths have
    // no stroke attrs of their own and rely on inheritance from the parent svg.
    const STROKE_ATTRS = 'fill="none" stroke="currentColor" stroke-width="2" stroke-linecap="round" stroke-linejoin="round"';
    for (const name of names) {
        try {
            const { viewBox, inner } = await fetchIcon(name);
            symbols.push(`  <symbol id="icon-${name}" viewBox="${viewBox}" ${STROKE_ATTRS}>${inner}</symbol>`);
            process.stdout.write('.');
        } catch (err) {
            process.stdout.write('\n');
            console.error(`  FAIL "${name}": ${err.message}`);
            throw err;
        }
    }
    process.stdout.write('\n');

    const sprite = [
        '<?xml version="1.0" encoding="UTF-8"?>',
        '<!-- DL-312: Generated by scripts/build-icon-sprite.mjs from Lucide v' + LUCIDE_VERSION + ' — do not edit by hand -->',
        '<svg xmlns="http://www.w3.org/2000/svg" style="display:none" aria-hidden="true">',
        '  <defs>',
        ...symbols,
        '  </defs>',
        '</svg>',
        '',
    ].join('\n');

    mkdirSync(dirname(OUT_PATH), { recursive: true });
    writeFileSync(OUT_PATH, sprite, 'utf8');
    console.log(`Wrote ${symbols.length} symbols to ${OUT_PATH} (${(sprite.length / 1024).toFixed(1)} KB)`);
}

main().catch((err) => {
    console.error(err);
    process.exit(1);
});
